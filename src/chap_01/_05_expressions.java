package chap_01;

public class _05_expressions {
    public static void main(String[] args) {
        // expression = operands & operators
        // operands = values, variables, numbers, quantity
        // operators = + - * / %

        int friends = 10;

        friends++;
        friends--;
        friends *= 2;

        double n = 10;
        n = (double) n / 3;

        System.out.println(friends);
        System.out.println(n);
    }
}
